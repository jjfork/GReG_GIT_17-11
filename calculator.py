def add(x, y):
    return x + y


def subtract(x, y):
    return x-y


def divide(x, y):
    return x/y


def multiply(x, y) -> int:
    return x * y


def square(x):
    return x**2


def power(x, y):
    return x**y


def sqrt(x):
    return x ** 0.5


def power_of(base, exponent):
    result = pow(base,exponent)
    return result


